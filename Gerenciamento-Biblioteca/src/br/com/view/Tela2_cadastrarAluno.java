    private void jBcadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBcadastrarActionPerformed
        String matricula = jTmatricula.getText();
        String nome = null;
        String serie = null;
        boolean veri = true, veri2 = true;

        if (jTnome.getText().trim().equals("") || jTmatricula.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Algum campo est√° vazio.");
        } else {
            try {
                Integer.valueOf(matricula);
                nome = jTnome.getText();
                serie = (String) jCBserie.getSelectedItem() + "" + jCBturma.getSelectedItem();
                veri = true;
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Erro no campo 'Matr√≠cula'");
                veri = false;
            }
            if (veri) {
                
                if (jTmatricula.getText().length() == 6) {
                    for (Aluno aluno : lista.getAlunos()) {
                        if (jTmatricula.getText().equals(aluno.getMatricula())) {
                            veri2 = false;
                        }
                    }
                    if (veri2) {
                        lista.armazenaAluno(matricula, nome, serie, "---");

                        tabelaRecentes(lista);

                        jTnome.setText(null);
                        jTmatricula.setText(null);
                    } else {
                        JOptionPane.showMessageDialog(null, "J√° existe um Aluno com essa matr√≠cula.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "A matr√≠cula deve ter 6 d√≠gitos.");
                }
            }
        }
    }//GEN-LAST:event_jBcadastrarActionPerformed

